/* Interactive zij Canon table.
   Loads a snapshot of the optimizer Canon (assets/json/zij_optimizers.json)
   and renders a searchable, filterable table on the zij project page.
   Vanilla JS, no dependencies. Data is generated by _scripts/generate_zij_data.py. */
(function () {
  "use strict";

  var root = document.getElementById("zij-canon");
  if (!root) return;

  var src = root.getAttribute("data-src");
  var searchEl = document.getElementById("zij-search");
  var chipsEl = document.getElementById("zij-chips");
  var metaEl = document.getElementById("zij-meta");
  var rowsEl = document.getElementById("zij-rows");
  var implEl = document.getElementById("zij-impl-only");

  var DATA = null;
  var activeCat = "All";

  function esc(s) {
    if (s == null) return "";
    return String(s).replace(/[&<>"]/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c];
    });
  }

  function link(url, text, cls) {
    if (!url) return esc(text);
    return '<a href="' + esc(url) + '" target="_blank" rel="noopener"' +
      (cls ? ' class="' + cls + '"' : "") + ">" + esc(text) + "</a>";
  }

  function matches(o, q) {
    if (activeCat !== "All" && o.category !== activeCat) return false;
    if (implEl && implEl.checked && !o.implemented) return false;
    if (!q) return true;
    var hay = (o.name + " " + (o.venue || "") + " " + (o.paper_title || "") + " " +
      o.category + " " + o.zij_classes.join(" ")).toLowerCase();
    return q.split(/\s+/).every(function (t) { return hay.indexOf(t) !== -1; });
  }

  function renderRows() {
    var q = (searchEl.value || "").trim().toLowerCase();
    var out = [];
    var shown = 0;
    for (var i = 0; i < DATA.optimizers.length; i++) {
      var o = DATA.optimizers[i];
      if (!matches(o, q)) continue;
      shown++;
      var cls = o.zij_classes.length
        ? o.zij_classes.map(function (c) { return '<code>' + esc(c) + "</code>"; }).join(" ")
        : '<span class="zij-dash">—</span>';
      var code = o.code_url
        ? link(o.code_url, o.code_type === "official" ? "official" : "code", "zij-code-link")
        : '<span class="zij-dash">—</span>';
      out.push(
        "<tr>" +
        '<td class="zij-c-name">' + (o.paper_url ? link(o.paper_url, o.name) : esc(o.name)) +
        (o.implemented ? ' <span class="zij-lead" title="Implemented in zij">zij</span>' : "") + "</td>" +
        '<td class="zij-c-cat">' + esc(o.category) + "</td>" +
        '<td class="zij-c-venue">' + esc(o.venue || "") + "</td>" +
        '<td class="zij-c-paper">' + (o.paper_title ? link(o.paper_url, o.paper_title) : '<span class="zij-dash">—</span>') + "</td>" +
        '<td class="zij-c-code">' + code + "</td>" +
        '<td class="zij-c-zij">' + cls + "</td>" +
        "</tr>"
      );
    }
    rowsEl.innerHTML = out.length
      ? out.join("")
      : '<tr><td colspan="6" class="zij-empty">No optimizers match. Try a different term or category.</td></tr>';
    metaEl.textContent = "Showing " + shown + " of " + DATA.totals.methods +
      " methods indexed here (" + DATA.totals.implemented + " with a zij class).";
  }

  function renderChips() {
    var cats = ["All"].concat(DATA.category_order.filter(function (c) {
      return DATA.category_counts[c];
    }));
    chipsEl.innerHTML = cats.map(function (c) {
      var n = c === "All" ? DATA.totals.methods : DATA.category_counts[c];
      return '<button type="button" class="zij-chip" data-cat="' + esc(c) + '">' +
        esc(c) + ' <span class="zij-chip-n">' + n + "</span></button>";
    }).join("");
    chipsEl.querySelectorAll(".zij-chip").forEach(function (btn) {
      btn.addEventListener("click", function () {
        activeCat = btn.getAttribute("data-cat");
        chipsEl.querySelectorAll(".zij-chip").forEach(function (b) {
          b.classList.toggle("active", b === btn);
        });
        renderRows();
      });
    });
    chipsEl.querySelector('.zij-chip[data-cat="All"]').classList.add("active");
  }

  function boot(data) {
    DATA = data;
    if (searchEl) {
      searchEl.placeholder = "Search " + data.totals.methods +
        " optimizers by name, venue, or class";
    }
    renderChips();
    renderRows();
    searchEl.addEventListener("input", renderRows);
    if (implEl) implEl.addEventListener("change", renderRows);
  }

  fetch(src)
    .then(function (r) { if (!r.ok) throw new Error(r.status); return r.json(); })
    .then(boot)
    .catch(function () {
      metaEl.innerHTML = 'Could not load the Canon snapshot. Browse it on the ' +
        '<a href="https://junaidaliop.github.io/zij/" target="_blank" rel="noopener">companion site</a>.';
    });
})();
